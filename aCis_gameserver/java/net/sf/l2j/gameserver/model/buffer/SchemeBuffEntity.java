package net.sf.l2j.gameserver.model.buffer;

public final class SchemeBuffEntity
{
	private int _buffId;
	private int _order;
	
	public int getBuffId()
	{
		return _buffId;
	}
	
	public void setBuffId(int buffId)
	{
		_buffId = buffId;
	}
	
	
	public int getOrder()
	{
		return _order;
	}
	
	public void setOrder(int order)
	{
		_order = order;
	}
}